type I18nValue = {
  language: Language;
  locale: string;
  setLanguage: (language: Language) => void;
  t: (key: TranslationKey) => string;
  zoneName: (zoneId: ZoneId) => string;
  categoryName: (category: "physical" | "cognitive") => string;
  statusName: (status: DecayStatus) => string;
  dataLabel: (value: string) => string;
};

const I18nContext = createContext<I18nValue | null>(null);

function initialLanguage(): Language {
  const saved = localStorage.getItem(STORAGE_KEY);
  if (saved === "en" || saved === "ru") return saved;
  return navigator.language.toLowerCase().startsWith("ru") ? "ru" : "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(initialLanguage);

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language;
    document.title = language === "ru" ? "Трекер прогресса" : "Progress Tracker";
  }, [language]);

  const value = useMemo<I18nValue>(() => {
    const dictionary = language === "ru" ? ru : en;
    return {
      language,
      locale: language === "ru" ? "ru-RU" : "en-US",
      setLanguage,
      t: (key) => dictionary[key],
      zoneName: (zoneId) => zoneNames[language][zoneId],
      categoryName: (category) => dictionary[`category.${category}`],
      statusName: (status) => dictionary[`status.${status}`],
      dataLabel: (label) => dataLabels[language][label] ?? label,
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}
